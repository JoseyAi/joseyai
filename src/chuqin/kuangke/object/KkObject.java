package chuqin.kuangke.object;

public class KkObject {
	public String kk_name,kk_number,kk_xibie,kk_banji,kk_course,kk_time,kk_reason;

	public String getName() {
		return kk_name;
	}

	public void setName(String kk_name) {
		this.kk_name = kk_name;
	}

	public String getNumber() {
		return kk_number;
	}

	public void setNumber(String kk_number) {
		this.kk_number = kk_number;
	}

	public String getXibie() {
		return kk_xibie;
	}

	public void setXibie(String kk_xibie) {
		this.kk_xibie = kk_xibie;
	}

	public String getBanji() {
		return kk_banji;
	}

	public void setBanji(String kk_banji) {
		this.kk_banji = kk_banji;
	}

	public String getCourse() {
		return kk_course;
	}

	public void setCourse(String kk_course) {
		this.kk_course = kk_course;
	}

	public String getTime() {
		return kk_time;
	}

	public void setTime(String kk_time) {
		this.kk_time = kk_time;
	}

	public String getReason() {
		return kk_reason;
	}

	public void setReason(String kk_reason) {
		this.kk_reason = kk_reason;
	}

}
