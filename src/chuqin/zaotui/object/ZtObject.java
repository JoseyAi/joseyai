package chuqin.zaotui.object;

public class ZtObject {
	public String zt_name,zt_number,zt_xibie,zt_banji,zt_course,zt_reason,zt_quxiang;

	public String getName() {
		return zt_name;
	}

	public void setName(String zt_name) {
		this.zt_name = zt_name;
	}

	public String getNumber() {
		return zt_number;
	}

	public void setNumber(String zt_number) {
		this.zt_number = zt_number;
	}

	public String getXibie() {
		return zt_xibie;
	}

	public void setXibie(String zt_xibie) {
		this.zt_xibie = zt_xibie;
	}

	public String getBanji() {
		return zt_banji;
	}

	public void setBanji(String zt_banji) {
		this.zt_banji = zt_banji;
	}
	
	public String getCourse() {
		return zt_course;
	}

	public void setCourse(String zt_course) {
		this.zt_course = zt_course;
	}

	public String getReason() {
		return zt_reason;
	}

	public void setReason(String zt_reason) {
		this.zt_reason = zt_reason;
	}

	public String getQuxiang() {
		return zt_quxiang;
	}

	public void setQuxiang(String zt_quxiang) {
		this.zt_quxiang = zt_quxiang;
	}

}
