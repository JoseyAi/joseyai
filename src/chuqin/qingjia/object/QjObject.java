package chuqin.qingjia.object;

public class QjObject {
	public String qj_name,qj_number,qj_xibie,qj_banji,qj_reason,qj_time,qj_fxtime;

	public String getName() {
		return qj_name;
	}

	public void setName(String qj_name) {
		this.qj_name = qj_name;
	}

	public String getNumber() {
		return qj_number;
	}

	public void setNumber(String qj_number) {
		this.qj_number = qj_number;
	}

	public String getXibie() {
		return qj_xibie;
	}

	public void setXibie(String qj_xibie) {
		this.qj_xibie = qj_xibie;
	}

	public String getBanji() {
		return qj_banji;
	}

	public void setBanji(String qj_banji) {
		this.qj_banji = qj_banji;
	}
	
	public String getReason() {
		return qj_reason;
	}

	public void setReason(String qj_reason) {
		this.qj_reason = qj_reason;
	}

	public String getTime() {
		return qj_time;
	}

	public void setTime(String qj_time) {
		this.qj_time = qj_time;
	}

	public String getFxtime() {
		return qj_fxtime;
	}

	public void setFxtime(String qj_fxtime) {
		this.qj_fxtime = qj_fxtime;
	}

}
