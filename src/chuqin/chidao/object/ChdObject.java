package chuqin.chidao.object;

public class ChdObject {
	public String name,number,xibie,banji,chd_time,chd_reason;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getXibie() {
		return xibie;
	}

	public void setXibie(String xibie) {
		this.xibie = xibie;
	}

	public String getBanji() {
		return banji;
	}

	public void setBanji(String banji) {
		this.banji = banji;
	}

	public String getTime() {
		return chd_time;
	}

	public void setTime(String chd_time) {
		this.chd_time = chd_time;
	}

	public String getReason() {
		return chd_reason;
	}

	public void setReason(String chd_reason) {
		this.chd_reason = chd_reason;
	}
}
